<?php

// PHP supports the following data types:

// 1. String
$name = "Ashikur Rahman";
// string always use single or double quotes

// 2. Integer
$age = 21;

// 3. float / double
$price = 99.99;

// 4. Boolean
$true = true;
$false = false;

// 5. Array
    // array are three types
$persons = ['Ashik', 'Ashik']; // Indexed Array
$persons = [
    'name' => "Ashik",
    'age' => 21
]; // Associative Array
$persons = [
    'ashik' => [
        'fullname' => 'Ashikur Rahman',
        'age' => 21
    ],
    'jahid' => [
        'fullname' => 'Jahid Wahid',
        'age' => 22
    ]
]; // Multidimensional arrays

// 6. Null
$name = null;

// 7. Object
// 8. Resource


